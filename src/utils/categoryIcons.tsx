export function getIconByName(name: string | null): LucideIcon | null {
  if (!name) return null;
  const iconOption = categoryIcons.find(opt => opt.name === name);
  return iconOption?.icon || null;
}

export function getIconsByCategory(category: string): CategoryIconOption[] {
  return categoryIcons.filter(opt => opt.category === category);
}

