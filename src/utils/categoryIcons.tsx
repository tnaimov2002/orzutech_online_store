export function suggestIconForCategory(categoryName: string): string {
  const normalizedName = categoryName.toLowerCase().trim();

  for (const [keyword, iconName] of Object.entries(keywordIconMap)) {
    if (normalizedName.includes(keyword)) {
      return iconName;
    }
  }

  const words = normalizedName.split(/[\s\-_,]+/);
  for (const word of words) {
    if (word.length < 2) continue;
    for (const [keyword, iconName] of Object.entries(keywordIconMap)) {
      if (keyword.includes(word) || word.includes(keyword)) {
        return iconName;
      }
    }
  }

  return 'package';
}

export function getSemanticIcon(iconName: string | null, categoryName: string): LucideIcon {
  if (iconName) {
    const icon = getIconByName(iconName);
    if (icon) return icon;
  }

  const suggestedIconName = suggestIconForCategory(categoryName);
  const suggestedIcon = getIconByName(suggestedIconName);

  return suggestedIcon || Package;
}
